          </span>
        </p>
        <p>
          Confirmation is a critical concept in cryptocurrencies. You could say that cryptocurrencies are all about confirmation.
        </p>
        <p>
          <span style="font-weight: 400;">
            As long as a transaction is unconfirmed, it is pending and can be forged. When a transaction is confirmed, it is set in stone. It is no longer forgeable, it can‘t be reversed, it is part of an immutable record of historical transactions: of the so-called <a rel="nofollow" href="http://blockgeeks.com/guides/what-is-blockchain-technology-a-step-by-step-guide-than-anyone-can-understand/">blockchain</a>.
          </span>
        </p>
        <p>
          <span style="font-weight: 400;">
            Only miners can confirm transactions. This is their job in a cryptocurrency-network. They take transactions, stamp them as legit and spread them in the network. After a transaction is confirmed by a miner, every node has to add it to its database. It has become part of the blockchain.
          </span>
        </p>
