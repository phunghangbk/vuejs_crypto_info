        <h2>
          <strong>
            What are miners doing?
          </strong>
        </h2>
        <p>&nbsp;</p>
        <p>
          <span style="font-weight: 400;">
            Principally everybody can be a miner. Since a decentralized network has no authority to delegate this task, a cryptocurrency needs some kind of mechanism to prevent one ruling party from abusing it. Imagine someone creates thousands of peers and spreads forged transactions. The system would break immediately.
          </span>
        </p>
        <p>
          <span style="font-weight: 400;">
            So, Satoshi set the rule that the miners need to invest some work of their computers to qualify for this task. In fact, they have to find a hash – a product of a cryptographic function – that connects the new block with its predecessor. This is called the <a href="https://blockgeeks.com/guides/proof-of-work-vs-proof-of-stake/">Proof-of-Work</a>. In Bitcoin, it is based on the <a rel="nofollow" href="https://en.wikipedia.org/wiki/SHA-2">SHA 256 Hash algorithm</a>.
          </span>
        </p>
        <p>&nbsp;</p>
        <p>
          <img class="alignnone wp-image-51106 size-large" src="https://blockgeeks.com/wp-content/uploads/2016/11/Icarus_Bitcoin_Mining_rig-1024x768-1-1024x768.jpg" alt="What is Cryptocurrency" width="640" height="480" srcset="https://blockgeeks.com/wp-content/uploads/2016/11/Icarus_Bitcoin_Mining_rig-1024x768-1.jpg 1024w, https://blockgeeks.com/wp-content/uploads/2016/11/Icarus_Bitcoin_Mining_rig-1024x768-1-300x225.jpg 300w, https://blockgeeks.com/wp-content/uploads/2016/11/Icarus_Bitcoin_Mining_rig-1024x768-1-768x576.jpg 768w" sizes="(max-width: 640px) 100vw, 640px">
        </p>
        <p>&nbsp;</p>
        <p>
          <span style="font-weight: 400;">
            You don‘t need to understand details about SHA 256. It‘s only important you know that it can be the basis of a <a href="https://blockgeeks.com/guides/cryptocurrencies-cryptography/">cryptologic puzzle</a> the miners compete to solve. After finding a solution, a miner can build a block and add it to the blockchain. As an incentive, he has the right to add a so-called coinbase transaction that gives him a specific number of Bitcoins. This is the only way to create valid Bitcoins.
          </span>
        </p>
        <p>
          <span style="font-weight: 400;">
            Bitcoins can only be created if&nbsp;miners solve a cryptographic puzzle. Since the difficulty of this puzzle increases the amount of computer power the whole miner’s invest, there is only a specific amount of cryptocurrency token that can be created in a given amount of time. This is part of the consensus no peer in the network can break.
          </span>
        </p>
