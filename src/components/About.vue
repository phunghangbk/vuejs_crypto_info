        <h2 style="text-align: center;">
          <b>
            What is cryptocurrency and how cryptocurrencies emerged as a side product of digital cash
          </b>
        </h2>
        <p>&nbsp;</p>
        <p>
          <span style="font-weight: 400;">
            Few people know, but cryptocurrencies emerged as a side product of another invention. Satoshi Nakamoto, the unknown inventor of 
            <a href="https://blockgeeks.com/guides/what-is-bitcoin-a-step-by-step-guide/">
              Bitcoin
            </a>
            , the first and still most important cryptocurrency, never intended to invent a currency.
          </span>
        </p>
        <p>
          <span style="font-weight: 400;">
            In his announcement of Bitcoin in late 2008, Satoshi said he developed “A Peer-to-Peer Electronic Cash System.“ &nbsp;
          </span>
        </p>
        <p>
          <span style="font-weight: 400;">
            His goal was to invent something; many people failed to create before digital cash.
          </span>
        </p>
