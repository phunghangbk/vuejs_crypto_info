        <p>&nbsp;</p>
        <p>
          Basically, cryptocurrencies are entries about token in decentralized consensus-databases. They are called <strong>CRYPTOcurrencies</strong> because the consensus-keeping process is secured by strong cryptography. Cryptocurrencies are built on <a href="https://blockgeeks.com/guides/cryptocurrencies-cryptography/">cryptography</a>. They are not secured by people or by trust, but by math. It is more probable that an asteroid falls on your house than that a bitcoin address is compromised.
        </p>
        <p>
          <span style="font-weight: 400;">
            Describing the properties of cryptocurrencies we need to separate between transactional and monetary properties. While most cryptocurrencies share a common set of properties, they are not carved in stone.
          </span>
        </p>
        <h2>
          <b>
            Transactional properties:
          </b>
        </h2>
        <p>&nbsp;</p>
        <p>
          <span style="font-weight: 400;">1.) </span>
          <b>Irreversible:</b>
          <span style="font-weight: 400;"> 
            After confirmation, a transaction can‘t be reversed. By nobody. And nobody means nobody. Not you, not your bank, not the president of the United States, not Satoshi, not your miner. Nobody. If you send money, you send it. Period. No one can help you, if you sent your funds to a scammer or if a hacker stole them from your computer. There is no safety net.
          </span>
        </p>
        <p>
          <span style="font-weight: 400;">
            2.) 
          </span>
          <b>
            Pseudonymous:
          </b>
          <span style="font-weight: 400;"> 
            Neither transactions nor accounts are connected to real-world identities. You receive Bitcoins on so-called addresses, which are randomly seeming chains of around 30 characters. While it is usually possible to analyze the transaction flow, it is not necessarily possible to connect the real world identity of users with those addresses.
          </span>
        </p>
        <p>
          <span style="font-weight: 400;">3.) </span>
          <b>Fast and global: </b>
          <span style="font-weight: 400;">
            Transaction are propagated nearly instantly in the network and are confirmed in a couple of minutes. Since they happen in a global network of computers they are completely indifferent of your physical location. It doesn‘t matter if I send Bitcoin to my neighbour or to someone on the other side of the world.
          </span>
        </p>
        <p>
          <span style="font-weight: 400;">4.) </span>
          <b>Secure:</b>
          <span style="font-weight: 400;"> 
            Cryptocurrency funds are locked in a public key cryptography system. Only the owner of the private key can send cryptocurrency. Strong cryptography and the magic of big numbers makes it impossible to break this scheme. A Bitcoin address is more secure than Fort Knox.
          </span>
        </p>
        <p>
          <span style="font-weight: 400;">5.) </span>
          <b>Permissionless</b>
          <span style="font-weight: 400;">
            : You don‘t have to ask anybody to use cryptocurrency. It‘s just a software that everybody can download for free. After you installed it, you can receive and send Bitcoins or other cryptocurrencies. No one can prevent you. There is no gatekeeper.
          </span>
        </p>
        <p>&nbsp;</p>
        <p>&nbsp;</p>
        <p>
          <img class="alignnone wp-image-51124 size-full" src="https://blockgeeks.com/wp-content/uploads/2016/11/infographics03-03-1.png" alt="What is Cryptocurrency" width="1984" height="1140" srcset="https://blockgeeks.com/wp-content/uploads/2016/11/infographics03-03-1.png 1984w, https://blockgeeks.com/wp-content/uploads/2016/11/infographics03-03-1-300x172.png 300w, https://blockgeeks.com/wp-content/uploads/2016/11/infographics03-03-1-768x441.png 768w, https://blockgeeks.com/wp-content/uploads/2016/11/infographics03-03-1-1024x588.png 1024w" sizes="(max-width: 1984px) 100vw, 1984px">
        </p>
