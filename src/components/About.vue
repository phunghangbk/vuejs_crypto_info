            <em>
              <strong>
                Announcing the first release of Bitcoin, a new electronic cash system that uses a peer-to-peer network to prevent double-spending. It’s completely decentralized with no server or central authority. &nbsp;
              </strong>
            </em>
            <em>
              <strong>
                – Satoshi Nakamoto, 09 January 2009, announcing Bitcoin on SourceForge.
              </strong>
            </em>
          </h3>
        </blockquote>
        <p>&nbsp;</p>
        <p>
          <span style="font-weight: 400;">
            The single most important part of Satoshi‘s invention was that he found a way to build a decentralized digital cash system. In the nineties, there have been many attempts to create digital money, but they all failed.
          </span>
        </p>
        <p>&nbsp;</p>
        <blockquote>
