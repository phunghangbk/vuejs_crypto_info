          – Erik Voorhees,&nbsp;<a href="https://shapeshift.io/#/coins" rel="nofollow" data-saferedirecturl="https://www.google.com/url?hl=en&amp;q=https://shapeshift.io/%23/coins&amp;source=gmail&amp;ust=1479230847343000&amp;usg=AFQjCNHRBTdY5HKq7fnO2CV-rchwcetLrA">cryptocurrency entrepreneur</a>
